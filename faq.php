                    <li>A number of videos exist from before widely accessable video editing that show Sasquatch clearly enough to not be a a black bear,
                        which is commonly mistaken for Bigfoot when one stands on its hind legs.</li>
                    <li>Many footprint castings have an extraordinary amount of detail that would be incredibly difficult to fake accurately.
                        This includes very fine toe print patterns and the footprints having the correct depth for a creature of its height and weight.</li>
                </ul>

