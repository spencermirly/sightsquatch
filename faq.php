                <p>What is Sasquatch/Bigfoot?</p>
                <p>Bigfoot (also known as Sasquatch) is a North American cryptid.
                    According to most accounts, Bigfoot lives in the forests, is covered in dark brown hair, and stands somewhere between 6-10 ft tall.</p>
                
                <p>What are Sasquatch encounters usually like?</p>
                <p>Sasquatch encounters can take many forms, Although some common types include the following:</p>
                <ul>
                    <li>Seeing a large, hairy, ape-like creature standing at a distance either watching or slowly moving through the forest</li>
                    <li>Finding one or more large, humanioid footprints much larger than any person would make</li>
                    <li>Hearing loud vocalizations in the forest that don't seem to match any other creatures in the area</li>
                </ul>

                <p>Isn't Sasquatch just a myth or a hoax?</p>
